package methodoverloading;

public class Looping {

	public static void main(String[] args) {
		
		for(int i= 0; i<10; i++) {
			System.out.println(i);
			
		}
		
		for (int i=1; i<=50;i++) {
			System.out.println(i);
		}
		for(int j =-10; j<=10;j++) {
			System.out.println(j);
		}
		for(int j =10; j>=-10;j--) {
			System.out.println(j);
		}
	}
	}

